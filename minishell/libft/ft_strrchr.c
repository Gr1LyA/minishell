#include "libft.h"

char	*ft_strrchr(const char *str, int ch)
{
	int	i;

	i = ft_strlen(str);
	while (i >= 0)
	{
		if (str[i] == (unsigned char)ch)
			return ((char *)(str + i));
		i--;
	}
	return (NULL);
}
